    private void boton_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_registrarActionPerformed
        modelo = txt_modelo.getText().trim();
        serie = txt_serie.getText().trim();
        tipo = box_tipo.getSelectedItem().toString();
        marca = box_marca.getSelectedItem().toString();
        observaciones = observaciones_Cap.getText().trim();
        empleado = Login.user;
        if(!modelo.isEmpty()&&!serie.isEmpty()&&!observaciones.isEmpty()){
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                    "insert into equipo values(?,?,?,?,?,?,?,?,?,?,?,?,?,?)");
            pst.setString(1, "0");
            pst.setString(2, id_cliente);
            pst.setString(3, tipo);
            pst.setString(4, marca);
            pst.setString(5, modelo);
            pst.setString(6, serie);
            pst.setString(7, dia);
            pst.setString(8, mes);
            pst.setString(9, annio);
            pst.setString(10, observaciones);
            pst.setString(11, "Nuevo Ingreso");
            pst.setString(12,empleado);
            pst.setString(13, " ");
            pst.setString(14, "");
            pst.executeUpdate();
            cn.close();
            txt_modelo.setText("");
            txt_nombre.setText("");
            txt_serie.setText("");
            observaciones_Cap.setText("");
            JOptionPane.showMessageDialog(null, "Los datos fueron Ingresados correctamente");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error con la conexion a la base de datos", "Error", JOptionPane.ERROR_MESSAGE);
        }
        }else{
            if (modelo.isEmpty()) {
                txt_modelo.setBackground(Color.red);
            }if (serie.isEmpty()) {
                txt_serie.setBackground(Color.red);
            }if(observaciones.isEmpty()){
                observaciones_Cap.setText("no hay observaciones");
            }
            JOptionPane.showMessageDialog(null, "Campos Vacios", "error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_boton_registrarActionPerformed

