    //esto es la logica para poder iniciar sesion
    private void ingresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingresarActionPerformed

        user = txt_usuario.getText().trim();
        pass = txt_password.getText().trim();

        //esta estructura es para validar que no se dejen espacios vacios
        if (!user.isEmpty() && !pass.isEmpty()) {

            try {
                //esto es una llamada a el metodo de conexion para no escribir todo el codigo de la conexion
                //con la base de datos
                Connection cn = Conexion.conectar();
                //esta linea sirve para hacer una llamada a la tabla empleados para que consiga la variable estatus y tipo
                //de nivel donde el nombre y la contrase単a corresponden a las que se ingresaron
                PreparedStatement pst = cn.prepareStatement(
                   "select tipo_nivel , estatus from empleados where username = '" + user
                        + "' and password = '" + pass + "'");
                
                ResultSet rs = pst.executeQuery();
                
                if (rs.next()) {
                    //este codigo verifica cual es el nivel dentro de el negocio y dependiendo de eso lo envia a una interfaz
                    //o a otra
                    String tipo_nivel =rs.getString("tipo_nivel");
                    String estatus = rs.getString("estatus");
                    if (estatus.equals("Activo")) {
                    if (tipo_nivel.equals("Administrador")) {
                        dispose();
                        new Administrador().setVisible(true);
                    } else if (tipo_nivel.equals("Capturista")){
                        dispose();
                        new Capturista().setVisible(true);
                    }else if(tipo_nivel.equals("Tecnico")){
                        dispose();
                        new Tecnico().setVisible(true);
                    }}
                } else {
                    //esto indica que si los datos osea el usuario y la contrase単a no son correctos entonces mandara a el usuario
                    //un mensaje para avisarle
                    JOptionPane.showMessageDialog(null, "Datos de Acceso Incorrectos");
                    txt_usuario.setText("");
                    txt_password.setText("");
                }
                
            } catch (SQLException e) {
                //esto notifica si por alguna razon el boton tiene problemas
                System.err.println("Error en el boton Acceder."+e);
                JOptionPane.showMessageDialog(null, "error al iniciar sesion, notifique al administrador");
            }

        } else {
            JOptionPane.showMessageDialog(null, "debes rellenar todos los campos");
        }

    }//GEN-LAST:event_ingresarActionPerformed

